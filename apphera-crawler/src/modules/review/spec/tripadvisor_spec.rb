describe Tripadvisor, "#valid?" do

  it "returns false for all bad urls" do
    @scraper = Scraper.new
    bad.split("\n").each do |url|
      job = Job.new({"content_provider" => "tripadvisor", "module_type" => "review", "url" => url}.to_json)
      @scraper.set_job job
      @scraper.valid?(job.url).should eq(false)
    end
  end

  it "returns false for all maybe urls" do
    @scraper = Scraper.new
    maybe.split("\n").each do |url|
      job = Job.new({"content_provider" => "tripadvisor", "module_type" => "review", "url" => url}.to_json)
      @scraper.set_job job
      @scraper.valid?(job.url).should eq(false)
    end
  end

  it "returns true for all good urls" do
    @scraper = Scraper.new
    good.split("\n").each do |url|
      job = Job.new({"content_provider" => "tripadvisor", "module_type" => "review", "url" => url}.to_json)
      @scraper.set_job job
      @scraper.valid?(job.url).should eq(true)
    end
  end

  it "completes full run for all good urls" do
    @scraper = Scraper.new
    good.split("\n").each do |url|
      job = Job.new({"content_provider" => "tripadvisor", "module_type" => "review", "url" => url}.to_json)
      @scraper.set_job job
      @scraper.run
    end
  end
end